package arrayprogramms01092022;

public class P1 {

	public static void main(String[] args) {
		int[][]a= {{1,2,3},{4,5,6}};
		
		System.out.println(a);            //it'll give address of an array
		System.out.println(a[0]);           //it'll give address of an single level array
		System.out.println(a[0][1]);         //it'll give address of an element of an array
	}

}
